    private long convertNumber(String input) throws NumberFormatException,
            NullPointerException {
        int radix;

        switch (NumSysRBGroup.getSelection().getActionCommand()) {
            case "bin":
                radix = 2;
                break;
            case "oct":
                radix = 8;
                break;
            case "dec":
                radix = 10;
                break;
            case "hex":
                radix = 16;
                break;
            default:
                radix = 10;
        }

        return Long.valueOf(input, radix);
    }

    private void toOctButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toOctButtonActionPerformed
        String input = InputField.getText();

        try {
            String output = Long.toOctalString(convertNumber(input));
            OutputField.setText(output);
        } catch (NumberFormatException | NullPointerException ex) {
            OutputField.setText("ERROR");
        }
    }//GEN-LAST:event_toOctButtonActionPerformed

